// In Js, Statements are executed from top to bottom
// We can just write bunch of statement to be executed and js will do the same from top to bottom
// But we can control the flow of execution by embedding some condition in the program
// We can do that by using some key words like if/elseif/else
/* It goes like this, 
if(any condition){
    statements to be executed on that condition
} 
elseif(another condition){
    statements to be executed on another condition
    executed when above condition is not meet
}
else{
    statements to be executed if none of the above two meets the condition
}
*/